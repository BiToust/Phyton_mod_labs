# Ввод длины стороны квадрата
side = float(input())

# Периметр квадрата — 4 * сторона
p = 4 * side

# Площадь квадрата — сторона^2
s = side * side

# Диагональ — сторона * sqrt(2), корень считаю через степень 0.5
d = (2 ** 0.5) * side

# Округляю до двух знаков с помощью форматирования
print(f"{p:.2f}, {s:.2f}, {d:.2f}")
